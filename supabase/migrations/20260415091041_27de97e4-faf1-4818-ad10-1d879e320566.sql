-- Also update the cask_sales table asking prices to match
UPDATE public.cask_sales cs
SET total_asking_price = c.total_price,
    asking_price_per_liter = c.price_per_liter,
    volume_for_sale_liters = c.current_volume_liters
FROM public.casks c
JOIN public.cask_ownership co ON co.cask_id = c.id
WHERE cs.ownership_id = co.id AND cs.status = 'active';
